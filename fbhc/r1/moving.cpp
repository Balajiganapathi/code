constexpr int mx_n = 102, mx_k = 5003;
ll dist[mx_n][mx_n];
int n, k;
int s[mx_k], d[mx_k];
ll dp[mx_k][mx_n][2];

ll solve(int idx, int last, int pending) {
    if(idx == k + 1) return pending? oo: 0;
    ll &ret = dp[idx][last][pending];
    if(ret != -1) return ret;
    ret = oo;

    int nxt = -1;
    ll cost = 0;
    if(pending) {
        // pending load, drive from last, pickup from idx then deliver previous load
        cost = dist[last][s[idx]];
        cost = min(cost + dist[s[idx]][d[idx-1]], oo);
        nxt = d[idx-1];
    } else {
        // no pending load, drive from last, pickup from idx
        cost = dist[last][s[idx]];
        nxt = s[idx];
    }

    // deliver now, no pending
    ret = min(ret, min(cost + dist[nxt][d[idx]], oo) + solve(idx + 1, d[idx], 0));
    // deliver later, pending
    ret = min(ret, cost + solve(idx + 1, nxt, 1));
    
    //trace(idx, last, pending, ret);
    return ret;
}

